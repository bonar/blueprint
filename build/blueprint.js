var BluePrint = /** @class */ (function () {
    function BluePrint() {
    }
    BluePrint.insert = function (url, width, height) {
        if (!url) {
            throw new Error("specify image url");
        }
        if (this.element) {
            this.updateBackgroundUrl(url);
        }
        else {
            this.insertImage(url);
        }
        this.element.style.width = width + 'px';
        this.element.style.height = height + 'px';
    };
    BluePrint.setOpacity = function (opacity) {
        this.assertElement();
        this.element.style.opacity = opacity;
    };
    BluePrint.setOffset = function (_top, left) {
        this.assertElement();
        this.element.style.top = _top;
        this.element.style.left = left;
    };
    BluePrint.show = function () {
        this.element.style.visibility = "visible";
    };
    BluePrint.hide = function () {
        this.element.style.visibility = "hidden";
    };
    BluePrint.insertImage = function (url) {
        this.element = document.createElement('div');
        this.updateBackgroundUrl(url);
        this.setBaseStyle(BluePrint.element);
        document.body.appendChild(this.element);
    };
    BluePrint.updateBackgroundUrl = function (url) {
        this.element.style.backgroundImage = 'url(' + url + ')';
    };
    BluePrint.setBaseStyle = function (img) {
        img.style.position = "absolute";
        img.style.top = "0";
        img.style.left = "0";
        img.style.zIndex = "999";
        img.style.opacity = "0.5";
    };
    BluePrint.assertElement = function () {
        if (!this.element) {
            throw new Error("call insertImage(url) at first");
        }
    };
    BluePrint.ELEMENT_ID = 'blueprint-autogenerated-layer';
    return BluePrint;
}());
